     */
    public function store(Request $request)
    {
        //
        User::create([
            'name'=>request('name'),
            'email'=>request('email'),
            'puesto'=>request('puesto'),
            'area_id'=>request('area_id'),
            'password'=>bcrypt(\request('password')),
        ]);
        return redirect()->route('lista.Usuario');
    }



